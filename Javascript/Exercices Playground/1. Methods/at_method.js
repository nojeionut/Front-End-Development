// learning about the at() method
// The at() method returns the element at the specified index in a string.

// Example 1
let str = 'Hello World!';
console.log(str.at(0)); // H
console.log(str.at(1)); // e
console.log(str.at(2)); // l
console.log(str.at(3)); // l
console.log(str.at(4)); // o
console.log(str.at(5)); //
console.log(str.at(6)); // W
console.log(str.at(7)); // o
console.log(str.at(8)); // r
console.log(str.at(9)); // l
console.log(str.at(10)); // d
console.log(str.at(11)); // !

const hobbies = ["coding", "reading", "writing", "playing"];
console.log(hobbies.at(0)); // coding
console.log(hobbies.at(1)); // reading
console.log(hobbies.at(2)); // writing
console.log(hobbies.at(3)); // playing
